    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        Nombre.setText("");
        Area.setText("");
        boolean var = true;
        do {
            int result = JOptionPane.showConfirmDialog(this, obtenerP("Categoria"),
                    "Nueva Categoria", JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.PLAIN_MESSAGE);

            if (result == JOptionPane.YES_OPTION) {

                if (Nombre.getText().compareTo("") == 0) {
                    JOptionPane.showMessageDialog(this, "No puede dejer en Blanco la categoria");
                    var = true;
                } else {
                    Categoria c = new Categoria();
                    c.setCategoria(Nombre.getText());
                    c.setDescripcion(Area.getText());
                    int r = JOptionPane.showConfirmDialog(this, "La categoria se guardara en la base de datos",
                            "Aviso", JOptionPane.OK_CANCEL_OPTION,
                            JOptionPane.WARNING_MESSAGE);
                    if (r == JOptionPane.YES_NO_OPTION) {
                        cp.crearCategoria(c);
                        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(cp.getCategorias()));
                        jComboBox1.setSelectedIndex(jComboBox1.getItemCount() - 1);
                        JOptionPane.showMessageDialog(this, "No podra elegir ni crear otra Categoria");
                        jComboBox1.setEnabled(false);
                        jButton1.setEnabled(false);

                        var = false;
                    } else {
                        var = false;

                    }

                }
            } else {
                var = false;
            }
        } while (var);
    }//GEN-LAST:event_jButton1ActionPerformed
    public JPanel obtenerP(String N1) {
        JLabel lblName = new JLabel(N1);
        JLabel lblAddress = new JLabel("Descripcion");
        final SpringLayout layout = new SpringLayout();

        final JPanel panel = new JPanel(layout);
        panel.setPreferredSize(new Dimension(250, 160));

        panel.add(lblName);

        Nombre.setBorder(BorderFactory.createLineBorder(Color.black));
        panel.add(Nombre);

        panel.add(lblAddress);
        Area.setBorder(BorderFactory.createLineBorder(Color.black));
        Area.setLineWrap(true);
        Area.setWrapStyleWord(true);
        JScrollPane scrollPane = new JScrollPane(Area,
                JScrollPane.VERTICAL_SCROLLBAR_ALWAYS,
                JScrollPane.HORIZONTAL_SCROLLBAR_ALWAYS);
        scrollPane.setPreferredSize(new Dimension(250, 100));
        panel.add(scrollPane);
        layout.putConstraint(SpringLayout.WEST, lblName,
                0,
                SpringLayout.WEST, panel);
        layout.putConstraint(SpringLayout.NORTH, lblAddress,
                10,
                SpringLayout.SOUTH, lblName);

        layout.putConstraint(SpringLayout.WEST, Nombre,
                25,
                SpringLayout.EAST, lblName);
        layout.putConstraint(SpringLayout.NORTH, scrollPane,
                10,
                SpringLayout.SOUTH, lblAddress);
        return panel;
    }
