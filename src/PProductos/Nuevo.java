    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
                switch (tipoAccion) {
            case nPresentacion:
                cp.setPresentacion((String) jComboBox2.getSelectedItem());
                cp.nuevaPresentacion(this);
                
                break;
            case nProducto:
                cp.setCategoria((String) jComboBox1.getSelectedItem());
                cp.setPresentacion((String) jComboBox2.getSelectedItem());
                Producto p = new Producto();

                p.setNombre(jTextField1.getText());
                p.setMarca(jTextField2.getText());
                p.setDescripcion(jTextArea1.getText());
                cp.nuevoProducto(p, this);
                break;
            case nSucursales:
                ArrayList<Sucursal> s= new ArrayList<>();
                for(ProductoSucursal ps :cp.getPPresentacion().getProductoSucursalList() ){
                    s.add(ps.getSucursalid());
                }
                cp.setSucursal(s);
                cp.nuevaSucursal(this);
                
                break;
            default:
                break;

        }
       
        
    }//GEN-LAST:event_jButton4ActionPerformed

